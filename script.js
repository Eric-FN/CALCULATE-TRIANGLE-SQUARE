var bQ = prompt("insira o valor da base do quadrado:");
var hQ = prompt("Insira o valor da altura do quadrado:");
var formulaQuadrado = bQ * hQ;
alert(`o resultado da área do quadrado é: ${formulaQuadrado}`);

var bT = prompt("insira o valor da base do traingulo:");
var hT = prompt("insira o valor da altura do triangulo:");
var formulaTriangulo = (bT * hT) / 2;
alert(`o resultado da área do triangulo é: ${formulaTriangulo}`);
